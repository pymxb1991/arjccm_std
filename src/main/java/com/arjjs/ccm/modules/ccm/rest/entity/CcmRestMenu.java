package com.arjjs.ccm.modules.ccm.rest.entity;

import java.util.List;


public class CcmRestMenu {
	private String id;    
    private String name;    // 菜单名称
    private String parentId;// 父菜单id
    private String type;   //用于判断是否有子菜单
    private List<CcmRestMenu> children;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getParentId() {
		return parentId;
	}
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public List<CcmRestMenu> getChildren() {
		return children;
	}
	public void setChildren(List<CcmRestMenu> children) {
		this.children = children;
	}    
    
    
}
