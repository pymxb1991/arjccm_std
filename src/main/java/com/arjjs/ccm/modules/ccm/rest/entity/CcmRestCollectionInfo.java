package com.arjjs.ccm.modules.ccm.rest.entity;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

@ApiModel
public class CcmRestCollectionInfo {
	@ApiModelProperty(value="书籍信息UUID")
	private String bookId;		    // 书籍信息UUID
	@ApiModelProperty(value="用户信息UUID")
	private String userId;		    // 用户信息UUID
	public String getBookId() {
		return bookId;
	}
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
}
