package com.arjjs.ccm.modules.ccm.rest.entity;

import java.util.List;

import com.google.common.collect.Lists;

public class CcmRestCatalog {
	private String bookId;		// 书籍信息id
	private String code;		// 书籍信息编号
	private String name;		// 书籍信息名称
	private String content;		// 文章内容
	private String imageurl;	// 书籍图片
	private String isCollection;// 是否已经收藏
	private List<CcmRestCatalog> childrenList = Lists.newArrayList();// 子节点
	public String getBookId() {
		return bookId;
	}
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImageurl() {
		return imageurl;
	}
	public void setImageurl(String imageurl) {
		this.imageurl = imageurl;
	}
	public String getIsCollection() {
		return isCollection;
	}
	public void setIsCollection(String isCollection) {
		this.isCollection = isCollection;
	}
	public List<CcmRestCatalog> getChildrenList() {
		return childrenList;
	}
	public void setChildrenList(List<CcmRestCatalog> childrenList) {
		this.childrenList = childrenList;
	}
}
