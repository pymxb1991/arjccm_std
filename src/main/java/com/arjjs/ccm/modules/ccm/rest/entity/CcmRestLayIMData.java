package com.arjjs.ccm.modules.ccm.rest.entity;

import java.util.List;

public class CcmRestLayIMData {

	private CcmRestLayIMUser mine;	//群组下wo
	private List<CcmRestLayIMGroup> friend;	//群组下friend
	
	public CcmRestLayIMData(){
		
	}
	public CcmRestLayIMData(CcmRestLayIMUser mine, List<CcmRestLayIMGroup> friend) {
		super();
		this.mine = mine;
		this.friend = friend;
	}
	public CcmRestLayIMUser getMine() {
		return mine;
	}
	public void setMine(CcmRestLayIMUser mine) {
		this.mine = mine;
	}
	public List<CcmRestLayIMGroup> getFriend() {
		return friend;
	}
	public void setFriend(List<CcmRestLayIMGroup> friend) {
		this.friend = friend;
	}
	
	
	
}
