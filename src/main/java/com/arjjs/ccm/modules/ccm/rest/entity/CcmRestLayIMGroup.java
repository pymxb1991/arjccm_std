package com.arjjs.ccm.modules.ccm.rest.entity;

import java.util.List;


public class CcmRestLayIMGroup {

	private String groupname; //群组名
	private String id;   //群组id
	private String online;	//
	private List<CcmRestLayIMUser> list;	//群组下用户
	
	public CcmRestLayIMGroup() {
		super();
	}
	public CcmRestLayIMGroup(String groupname, String id, String online, List<CcmRestLayIMUser> list) {
		super();
		this.groupname = groupname;
		this.id = id;
		this.online = online;
		this.list = list;
	}
	
	public List<CcmRestLayIMUser> getList() {
		return list;
	}
	public void setList(List<CcmRestLayIMUser> list) {
		this.list = list;
	}
	public String getGroupname() {
		return groupname;
	}
	public void setGroupname(String groupname) {
		this.groupname = groupname;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOnline() {
		return online;
	}
	public void setOnline(String online) {
		this.online = online;
	}
	
	
}
