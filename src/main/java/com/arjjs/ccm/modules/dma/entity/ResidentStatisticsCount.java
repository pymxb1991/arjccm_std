package com.arjjs.ccm.modules.dma.entity;

import com.arjjs.ccm.common.persistence.DataEntity;

public class ResidentStatisticsCount extends DataEntity<ResidentStatisticsCount> {
	private static final long serialVersionUID = 1L;
	private Integer dataNum;	  // 数据数量
	private String areaId;	      // 数据所在区域
	private String areaParentIds; // 数据所在区域的父节点id群
	private String dateYear;	  // 数据修改年份
	private String dateMouth;	  // 数据修改月份
	private String dateQuarter;	  // 数据修改季度
	private String dateDay;	      // 数据修改日期
	private String dateWeek;	  // 数据修改某周
	private String peopleType;	  // 数据人员类型
	public ResidentStatisticsCount() {
		super();
		this.dataNum = 0;
	}
	public Integer getDataNum() {
		return dataNum;
	}
	public void setDataNum(Integer dataNum) {
		this.dataNum = dataNum;
	}
	public String getAreaId() {
		return areaId;
	}
	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}
	public String getAreaParentIds() {
		return areaParentIds;
	}
	public void setAreaParentIds(String areaParentIds) {
		this.areaParentIds = areaParentIds;
	}
	public String getDateYear() {
		return dateYear;
	}
	public void setDateYear(String dateYear) {
		this.dateYear = dateYear;
	}
	public String getDateMouth() {
		return dateMouth;
	}
	public void setDateMouth(String dateMouth) {
		this.dateMouth = dateMouth;
	}
	public String getDateQuarter() {
		return dateQuarter;
	}
	public void setDateQuarter(String dateQuarter) {
		this.dateQuarter = dateQuarter;
	}
	public String getDateDay() {
		return dateDay;
	}
	public void setDateDay(String dateDay) {
		this.dateDay = dateDay;
	}
	public String getDateWeek() {
		return dateWeek;
	}
	public void setDateWeek(String dateWeek) {
		this.dateWeek = dateWeek;
	}
	public String getPeopleType() {
		return peopleType;
	}
	public void setPeopleType(String peopleType) {
		this.peopleType = peopleType;
	}
}
