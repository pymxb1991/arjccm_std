package com.arjjs.ccm.modules.im.entity;

public class ImParamData {
	private String userId;
	private String id;
	private String type;
	private String skipToPage;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSkipToPage() {
		return skipToPage;
	}
	public void setSkipToPage(String skipToPage) {
		this.skipToPage = skipToPage;
	}
	
}
